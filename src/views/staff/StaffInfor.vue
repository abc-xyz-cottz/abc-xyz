<template>
  <div class="container">
    <b-card-group>
      <b-card no-body>
        <b-card-body>
          <b-form>
            <b-row>
              <b-col cols="8">
                <h4>Thông Tin Cá Nhân</h4>
              </b-col>
              <b-col cols="4">
                <b-button v-if="onEdit" variant="primary" class="px-4 float-right" @click="save">
                  Save
                </b-button>
                <b-button v-else variant="primary" class="px-4 float-right" @click="edit">
                  Edit
                </b-button>
              </b-col>
            </b-row>

            <div class="form-group">
              <label>Tên</label><span class="error-sybol"></span>
              <input
                id="name"
                v-model="inputs.name"
                type="text"
                autocomplete="new-password"
                class="form-control"
                maxlength="100"
                :disabled="!onEdit">
                <b-form-invalid-feedback  class="invalid-feedback" :state="!errorName">
                  Vui lòng nhập tên
                </b-form-invalid-feedback>
            </div>

            <div class="form-group">
              <label>Số Điện Thoại</label><span class="error-sybol"></span>
              <input
                id="phone"
                v-model="inputs.phone_number"
                type="text"
                autocomplete="new-password"
                class="form-control"
                maxlength="15"
                @keypress="validateCode"
                :disabled="!onEdit"
                v-on:change="checkPhoneNumberFormat($event.target)">
                <b-form-invalid-feedback class="invalid-feedback" :state="!phoneNumberCheckFlag || !errorPhone">
                  Vui lòng nhập số điện thoại
                </b-form-invalid-feedback>
                <b-form-invalid-feedback class="invalid-feedback" :state="phoneNumberCheckFlag">
                  Số điện thoại không đúng
                </b-form-invalid-feedback>
            </div>

            <div class="form-group">
              <label>Quyền</label><span class="error-sybol"></span>
              <input
                id="role"
                v-model="inputs.role_name"
                type="text"
                autocomplete="new-password"
                class="form-control"
                maxlength="15"
                readonly
                >
            </div>
          </b-form>
        </b-card-body>
      </b-card>
    </b-card-group>
  </div>
</template>

<script>
import CustomerAPI from '@/api/customer'
import StaffAPI from '@/api/admin'
import Mapper from '@/mapper/staff'
import commonFunc from '@/common/commonFunc'
import 'vue2-datepicker/index.css'


export default {
  name: 'Register',
  data () {
    return {
      inputs: {
        name: '',
        phone_number: '',
        role_name: ''
      },
      click: false,
      onEdit: false,
      phoneNumberCheckFlag: true,
    }
  },
  computed: {
    errorName: function () {
      return this.checkInfo(this.inputs.name)
    },
    errorPhone: function () {
      return this.checkInfo(this.inputs.phone)
    },
  },
  mounted () {
    this.getStaffInfo()
  },
  methods: {
    checkInfo (info) {
      return (this.click && (info == null || info.length <= 0))
    },
    checkValidate () {
      return !(this.errorName || this.errorPhone || !this.phoneNumberCheckFlag)
    },

    /**
   * Make toast without title
   */
    popToast(variant, content) {
      this.$bvToast.toast(content, {
        toastClass: 'my-toast',
        noCloseButton: true,
        variant: variant,
        autoHideDelay: 3000
      })
    },

    /**
     * Get staff information
     */
    getStaffInfo () {
      let staffId = this.$store.state.user.id
      StaffAPI.getStaffDetail(staffId).then(res => {
        if(res != null && res.data != null && res.data.data != null){
          this.inputs = Mapper.mapStaffDetailModelToDto(res.data.data)
        }
      }).catch(err => {
        // Handle error
          let errorMess = commonFunc.handleCusError(err)
          this.popToast('danger', errorMess)
      })
    },

    /**
     * Validate code
     */
    validateCode (event) {
      // not number
      if(!commonFunc.isNumber(event)) {
        event.preventDefault()
      }
    },

    /**
     * Event edit
     */
    edit() {
      this.onEdit = true
    },


    /**
     * Save infor
     */
    save() {
      this.click = true
      let result = this.checkValidate()
      if(result) {
        this.formatBirthday()
         CustomerAPI.updateInfo(this.inputs).then(res => {
           this.formatBirthday()
          if(res != null && res.data != null) {
            if (res.data.status == 200) {
              // show popup success
              this.popToast('success', 'Cập nhật thông tin thành công!!! ')
              this.onEdit = false
            }
          }
        }).catch(err => {
          this.formatBirthday()
          let message = ""
          if(err.response.data.status == 422) {
            message = err.response.data.mess
          } else {
            message = "Lỗi hệ thống"
          }
          this.$bvModal.msgBoxOk(message, {
            title: "Cập Nhật Thông Tin",
            centered: true,
            size: 'sm',
            headerClass: 'bg-danger',
          })
        })
      }
    },

    /**
     * Check phone number
     */
    checkPhoneNumberFormat(item) {
      let valueInput = item.value
      if (valueInput != null && valueInput != "") {
        if (commonFunc.phoneNumberCheck(valueInput)) {
          this.phoneNumberCheckFlag = true
        } else {
          this.phoneNumberCheckFlag = false
        }
      } else if(this.errorPhone) {
          this.phoneNumberCheckFlag = false
      } else {
        this.phoneNumberCheckFlag = true
      }
    },
  }
}
</script>
